score = 0

# let's say the player just
# scored 5 points
score = score + 5

# above is equivalent to below
score += 5 # score is increased by 5
print(score)

# score = 5 is not the same! You're just assigning the value 5 to score

# other augmented assignment operators
score -= 3
print(score)

score *= 15
print(score)

score /= 3
print(score)

score //= 3
print(score)

score %= 4
print(score)

score **= 2
print(score)